function setLang(l) {
  lang = l;
  document.documentElement.setAttribute('lang', l);

  document.querySelectorAll('.lbtn').forEach(function(b) {
    b.classList.toggle('active', b.textContent.trim() === l.toUpperCase());
  });

  var t = T[l];
  document.querySelectorAll('[data-k]').forEach(function(el) {
    var k = el.getAttribute('data-k');
    if (t[k] !== undefined) el.innerHTML = t[k];
  });
  document.querySelectorAll('[data-ph]').forEach(function(el) {
    var k = el.getAttribute('data-ph');
    if (t[k]) el.placeholder = t[k];
  });

  var panel = document.getElementById('brand-strip-panel');
  if (panel) {
    panel.classList.remove('bsl-uz', 'bsl-ru', 'bsl-en');
    panel.classList.add(stripClass[l] || 'bsl-uz');
    var lbl = document.getElementById('bsl-lang-label');
    if (lbl && t.bsl_lang_label) lbl.textContent = t.bsl_lang_label;
  }
}


/* ── SCROLL REVEAL ── */
(function() {
  var els = document.querySelectorAll('.reveal');
  els.forEach(function(el) { el.classList.add('will-animate'); });

  function show(el) {
    el.classList.remove('will-animate');
    el.classList.add('animated');
  }

  if ('IntersectionObserver' in window) {
    var io = new IntersectionObserver(function(entries) {
      entries.forEach(function(e) {
        if (e.isIntersecting) { show(e.target); io.unobserve(e.target); }
      });
    }, { threshold: 0.05, rootMargin: '0px 0px -20px 0px' });
    els.forEach(function(el) { io.observe(el); });
  } else {
    els.forEach(show);
  }
})();


/* ── CONTACT FORM ── */
var BACKEND_URL = '/api/contact';

async function submitForm(e) {
  e.preventDefault();

  var form   = document.getElementById('contactForm');
  var btn    = form.querySelector('.btn-form');
  var inputs = form.querySelectorAll('input, textarea, select');

  var data = {
    name:    inputs[0].value.trim(),
    company: inputs[1].value.trim(),
    contact: inputs[2].value.trim(),
    phone:   inputs[3].value.trim(),
    service: inputs[4].value.trim(),
    message: inputs[5].value.trim(),
  };

  if (!data.name || !data.contact) return;

  var originalText = btn.textContent;
  btn.textContent = '⏳ Yuborilmoqda...';
  btn.disabled = true;

  try {
    var res  = await fetch(BACKEND_URL, {
      method:  'POST',
      headers: { 'Content-Type': 'application/json' },
      body:    JSON.stringify(data),
    });
    var json = await res.json();

    if (json.ok) {
      form.style.display = 'none';
      document.getElementById('formSuccess').style.display = 'block';
    } else {
      throw new Error(json.error || 'Server error');
    }
  } catch (err) {
    console.error('Submit error:', err);
    btn.textContent = '❌ Xato yuz berdi. Qayta urining.';
    btn.disabled = false;
  }
}


/* ── MOBILE NAV ── */
(function() {
  var burger = document.getElementById('burger');
  var navlinks = document.getElementById('navlinks');
  if (!burger || !navlinks) return;

  // Ensure initial ARIA state
  burger.setAttribute('aria-expanded', 'false');
  navlinks.setAttribute('aria-hidden', 'true');

  function openNav() {
    navlinks.classList.add('open');
    burger.classList.add('open');
    burger.setAttribute('aria-expanded', 'true');
    navlinks.setAttribute('aria-hidden', 'false');
    document.body.classList.add('nav-open');
  }

  function closeNav() {
    navlinks.classList.remove('open');
    burger.classList.remove('open');
    burger.setAttribute('aria-expanded', 'false');
    navlinks.setAttribute('aria-hidden', 'true');
    document.body.classList.remove('nav-open');
  }

  burger.addEventListener('click', function(e) {
    e.stopPropagation();
    if (navlinks.classList.contains('open')) closeNav(); else openNav();
  });

  // Close when a nav link is chosen
  document.querySelectorAll('.nl').forEach(function(link) {
    link.addEventListener('click', function() { closeNav(); });
  });

  // Close on outside click
  document.addEventListener('click', function(e) {
    if (!navlinks.classList.contains('open')) return;
    if (navlinks.contains(e.target) || burger.contains(e.target)) return;
    closeNav();
  });

  // Close on Escape
  document.addEventListener('keydown', function(e) {
    if (e.key === 'Escape' && navlinks.classList.contains('open')) closeNav();
  });

  // If user resizes to desktop breakpoint, ensure nav closed
  window.addEventListener('resize', function() {
    if (window.innerWidth > 1000 && navlinks.classList.contains('open')) closeNav();
  });

})();
